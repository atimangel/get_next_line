#include "get_next_line.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *copy_s;

	copy_s = (unsigned char *)s;
	while (n)
		copy_s[--n] = 0;
}

size_t	ft_strlen(const char *s)
{
	size_t len;

	len = 0;
	while (s[len])
		len++;
	return (len);
}


